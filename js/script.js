    function calculateTotal() {
      const courses = {
        frontend: 2800, 
        backend: 3500, 
        design: 1900,  
      };

      let totalCost = 0;

      for (const course in courses) {
        const count = parseInt(document.querySelector(`[name="${course}Count"]`).value);
        totalCost += courses[course] * count;
      }

      if (totalCost > 3000) {
        const discount = 0.3;
        const discountedCost = totalCost * (1 - discount);
        document.getElementById('totalCost').innerText = `Уважаемый студент, вы должны заплатить $${totalCost.toFixed(2)}, но вы получаете 30% скидку, и окончательная сумма составляет $${discountedCost.toFixed(2)}`;
      } else {
        document.getElementById('totalCost').innerText = `Уважаемый студент, вы должны заплатить $${totalCost.toFixed(2)}`;
      }
    }
 
